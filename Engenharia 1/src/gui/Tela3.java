    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String nome, marca, preco, tipo, quantidade;

        nome = txtNome.getText();
        marca = txtMarca.getText();
        preco = txtPreco.getText();
        tipo = txtTipo.getSelectedItem().toString();
        quantidade = txtQuantidade.getText();


        mercadoDTO objmercadoDTO = new mercadoDTO();
        objmercadoDTO.setNome(nome);
        objmercadoDTO.setMarca(marca);
        objmercadoDTO.setPreco(preco);
        objmercadoDTO.setTipo(tipo);

       
        UsuarioDAO objmercadoDAO = new UsuarioDAO();
        objmercadoDAO.cadastrarMercado(objmercadoDTO);

        JOptionPane.showMessageDialog(null, "Cadastro feito!");

        dispose();

        Tela2 objListausergui = new Tela2();
        objListausergui.setVisible(true);  

        dispose();
    }//GEN-LAST:event_jButton2ActionPerformed

